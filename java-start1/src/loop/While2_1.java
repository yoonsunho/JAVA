package loop;

public class While2_1 {
    public static void main(String[] args) {

        int sum = 0;
        int i = 100;

        sum += i;
        System.out.println("i ="+i+"sum="+sum);
        i ++;

        sum += i;
        System.out.println("i ="+i+"sum="+sum);
        i++;

        sum += i;
        System.out.println("i ="+i+"sum="+sum);
    }

}
