package operator;

public class Operator1 {
    public static void main(String[] args) {
        int a = 2;
        int b = 5;

        int sum = a +b;
        System.out.println("a+b ="+sum);

        int multi = a*b;
        System.out.println("a*b = "+multi);

        int div = b / a;
        System.out.println("a/b = "+div);

        int mod =  b % a;
        System.out.println("b%a=" + mod);

        //int z = 10/0;

    }
}
